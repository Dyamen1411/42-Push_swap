#include "push_swap.h"

/* ************************************************************************** */
/*                                                                            */
/* Helper protoypes                                                           */
/*                                                                            */
/* ************************************************************************** */

static void	_do_nothing(
				void *_
				);

/* ************************************************************************** */
/*                                                                            */
/* Header implementation                                                      */
/*                                                                            */
/* ************************************************************************** */

void	ps_cleanup(
			t_ps_context **context_ptr
			)
{
	if (*context_ptr == NULL)
		return ;
	ft_lstclear(&(*context_ptr)->a, _do_nothing);
	ft_lstclear(&(*context_ptr)->b, _do_nothing);
	free((*context_ptr)->values);
	free(*context_ptr);
	*context_ptr = NULL;
}

/* ************************************************************************** */
/*                                                                            */
/* Helper implementation                                                      */
/*                                                                            */
/* ************************************************************************** */

static void	_do_nothing(
				void *_
				)
{
	(void)_;
}
