static void	_push(
				t_list **dst_ptr,
				t_list **src_ptr
				);

/* ************************************************************************** */
/*                                                                            */
/* Header implementation                                                      */
/*                                                                            */
/* ************************************************************************** */

void	ps_pa(
			t_ps_context *ctx
			)
{
	_push(&ctx->a, &ctx->b);
}

void	ps_pb(
			t_ps_context *ctx
			)
{
	_push(&ctx->b, &ctx->a);
}

/* ************************************************************************** */
/*                                                                            */
/* Helper implementation                                                      */
/*                                                                            */
/* ************************************************************************** */

static void	_push(
				t_list **dst_ptr,
				t_list **src_ptr
				)
{
	t_list	*tmp;

	if (*src_ptr == NULL)
		return ;
	tmp = *src_ptr;
	*src_ptr = (*src_ptr)->next;
	tmp->next = *dst_ptr;
	*dst_ptr = tmp;
}
